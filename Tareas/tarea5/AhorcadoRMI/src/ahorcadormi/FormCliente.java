    private void crearBotonesLetras() {
        int charCode = 65;
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 5; j++) {
                if (charCode <= 90) {
                    Character c = (char) charCode;
                    int y = 40 * i;
                    int x = 40 * j;
                    panelBotones.add(crearBoton(Character.toString(c), x, y));
                    charCode++;
                }
            }
        }
    }

    private void init() {
        try {
            ahorcado = (IAhorcado) Naming.lookup("rmi://localhost/Ahorcado");
        } catch (MalformedURLException | NotBoundException | RemoteException e) {
            System.out.println(e);
        }
    }

    private void iniciarJuego() {
        try {
            ahorcado.iniciar();
            labelPalabraOculta.setText(ahorcado.getPalabraOculta());
            labelNumeroIntentos.setText("NÃºmero de intentos: " + ahorcado.getNumeroIntentos());
            Component[] botones = panelBotones.getComponents();
            for (Component boton : botones) {
                boton.setEnabled(true);
            }
        } catch (RemoteException e) {
            System.out.println(e);
        }
    }

    private JButton crearBoton(String text, int x, int y) {
        JButton boton = new JButton();
        boton.setText(text);
        boton.setBounds(x, y, 40, 40);
        boton.setVisible(true);
        boton.addActionListener((ActionEvent e) -> {
            try {
                boton.setEnabled(false);
                if (ahorcado.intento(text.toLowerCase().charAt(0))) {
                    labelPalabraOculta.setText(ahorcado.getPalabraOculta());
                    if (ahorcado.esGanador()) {
                        JOptionPane.showMessageDialog(this, "Â¡Genial, ganaste el juego!");
                        iniciarJuego();
                    }
                } else {
                    int numeroIntentos = ahorcado.getNumeroIntentos();
                    labelNumeroIntentos.setText("NÃºmero de intentos: " + numeroIntentos);
                    if (numeroIntentos == 0) {
                        JOptionPane.showMessageDialog(this, "Ya no tiene intentos");
                        iniciarJuego();
                    }
                }
            } catch (RemoteException ex) {
                Logger.getLogger(FormCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        });
        return boton;
    }

